void HAL_I2C_MemTxCpltCallback (I2C_HandleTypeDef * hi2c)
{
  ssd1306_DMA_callback();
  if (ssd1306_IsUpdateFinished() && perpetualFlag){
    ssd1306_UpdateScreen();
    static uint32_t prevVal = 0;
    uint32_t curVal = HAL_GetTick();
    dur = curVal - prevVal;
    prevVal = curVal;
  }
}
